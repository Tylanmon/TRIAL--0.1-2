var language = document.getElementById('language');

var video = document.getElementById('video');

turkish.onmouseover = function(){
    video.setAttribute('src','Videos/Turkish.mp4')
    language.textContent = 'Turkish'
    language.style.color = 'red'

    japanese.style.opacity='40%'
    chinese.style.opacity='40%'
    french.style.opacity='40%'
    american.style.opacity='40%'
    british.style.opacity='40%'
    german.style.opacity='40%'
    russian.style.opacity='40%'

}



turkish.onmouseout = function(){
    video.setAttribute('src','Videos/Komp_1.mp4')
    language.textContent = 'Select the language of the march you want'
    language.style.color = 'black'

    japanese.style.opacity='100%'
    chinese.style.opacity='100%'
    french.style.opacity='100%'
    american.style.opacity='100%'
    british.style.opacity='100%'
    german.style.opacity='100%'
    russian.style.opacity='100%'
}

